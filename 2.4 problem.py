#Write a program to calculate the square of a number entered by the user.
n=float(input("Enter the number the find square of:"))
print("Square of",n,":",n*n)  # or n**2 is also valid and better as it gives free hand to increase the power in future if needed.
#Example of using power operator
print("Square of",n,":",n**2)
print("Cube of",n,":",n**3)
print("Fourth power of",n,":",n**4)
